package com.example.homework231004;

public class EnemyCircle {
    int x;
    int y;
    int way;

    public EnemyCircle(){
        this.way = 0;
    }

    public int getWay() {
        return way;
    }

    public void setWay(int way) {
        this.way = way;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
